package com.mark.cyberpunkplayer.scran;

public class ScranFileThread extends Thread{


    public ScranFileThread(String name) {
        super(name);
    }

    @Override
    public void run() {
        super.run();
    }

    @Override
    public void interrupt() {
        super.interrupt();
    }

    @Override
    public synchronized void start() {
        super.start();
    }


}
